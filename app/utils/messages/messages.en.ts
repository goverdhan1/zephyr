/**
 * Returns API path with dynamic values
 * @param key : string from I18N_MESSAGES
 * @param params: array of params that has to be replaced in the api path
 * @Input: getI18nText('GET_STEPS_BY_TESTCASE_ID', [1])
 * @Output: '/testcase/1/teststep'
 */
export var getI18nText =  function(key: any, params: any) {
	let _path = I18N_MESSAGES[key] || '';
    _.each(params, function (_param: any, i: any) {
        let _paramPatternText = '\$\{' + i + '\}';
        _path = (_path.replace(_paramPatternText, _param));
    });
    return _path;
};
